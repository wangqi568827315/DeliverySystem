package com.chinasoft.model.entity;

public class SALARY {

	private int s_no;
	private int e_no;
	private int s_basic;
	private int s_count;
	private double s_bonus;
	private double s_sum;
	private String s_date;
	
	public int getS_no() {
		return s_no;
	}
	public void setS_no(int s_no) {
		this.s_no = s_no;
	}
	public int getE_no() {
		return e_no;
	}
	public void setE_no(int e_no) {
		this.e_no = e_no;
	}
	public int getS_basic() {
		return s_basic;
	}
	public void setS_basic(int s_basic) {
		this.s_basic = s_basic;
	}
	public int getS_count() {
		return s_count;
	}
	public void setS_count(int s_count) {
		this.s_count = s_count;
	}
	public double getS_bonus() {
		return s_bonus;
	}
	public void setS_bonus(double s_bonus) {
		this.s_bonus = s_bonus;
	}
	public double getS_sum() {
		return s_sum;
	}
	public void setS_sum(double s_sum) {
		this.s_sum = s_sum;
	}
	public String getS_date() {
		return s_date;
	}
	public void setS_date(String s_date) {
		this.s_date = s_date;
	}
	
	
}
